    private void btnTampilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilActionPerformed
        buatTabel();
        tabel.getDataVector().removeAllElements();
        tabel.fireTableDataChanged();
        data.clear();
        String tglPertama=tf.format(tgl1.getDate());
        String tglKedua=tf.format(tgl2.getDate());        
        data=pm.tampilBerdasarkanTgl(tglPertama, tglKedua);
        for (int x = 0; x < data.size(); x++) {
            Object[] dataPinjam = new Object[5];
            dataPinjam[0] = data.get(x).getId();
            dataPinjam[1] = data.get(x).getNo_pinjam();
            dataPinjam[2] = data.get(x).getNama_anggota();
            dataPinjam[3] = data.get(x).getTgl_pinjam();
            dataPinjam[4] = data.get(x).getTgl_kembali();
            tabel.addRow(dataPinjam);
        }        
    }//GEN-LAST:event_btnTampilActionPerformed

