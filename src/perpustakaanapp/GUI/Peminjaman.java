    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        if (tblPinjam.getRowCount()!=0) {
            buatTabelBuku();
            tabel.getDataVector().removeAllElements();
            tabel.fireTableDataChanged();
            dataPinjamDetail.clear();
            dataPinjamDetail = pdm.tampilBuku(tblPinjam.getValueAt(tblPinjam.getSelectedRow(), 1).toString());
            for (int x = 0; x < dataPinjamDetail.size(); x++) {
                Object[] dataPinjam = new Object[4];
                dataPinjam[0] = dataPinjamDetail.get(x).getKode();
                dataPinjam[1] = dataPinjamDetail.get(x).getJudul();
                dataPinjam[2] = dataPinjamDetail.get(x).getPengarang();
                dataPinjam[3] = dataPinjamDetail.get(x).getPenerbit();
                tabel.addRow(dataPinjam);
            }
            dialogBuku.setSize(589, 276);
            dialogBuku.setLocationRelativeTo(this);
            dialogBuku.setVisible(true);
        }
    }//GEN-LAST:event_btnDetailActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int pesan=JOptionPane.showConfirmDialog(null, "Yakin data akan dihapus", "Pesan", JOptionPane.YES_NO_OPTION);
        if(pesan==JOptionPane.YES_OPTION){
            //kembalikan stok buku
            BukuModel bm=new BukuModel();
            int baris=tblPinjam.getSelectedRow();
            dataPinjamDetail=pdm.cariBuku(tblPinjam.getValueAt(baris, 0).toString());
            dataPinjamDetail.stream().forEach((d) -> {
                bm.kembalikanStokBuku(d.getId_buku(), 1);
            });
            pdm.setId_pinjam(tblPinjam.getValueAt(baris, 0).toString());
            pdm.hapus();
            pm.setId(tblPinjam.getValueAt(baris, 0).toString());
            pm.hapus();
            btnTampilActionPerformed(evt);
        }        
    }//GEN-LAST:event_btnHapusActionPerformed

